        <ol>
          <li>1. 가벼운 오염(비영구적 오염), 가벼운 생활 스크래치, 제품에 부착된 부속품이 떨어졌지만 부속품을 회수한 상태 등 원상태로 복구 가능한 손상의 경우 수선 비용은 회사에서 부담합니다.</li>
          <li>2. 영구적인 오염, 제품 원단 자체의 손상, 제품에 부착된 부속품이 떨어지고 부속품도 분실한 상태, 제품과 함께 동봉된 부속품(장신구)이 분실된 상태 등 원상태로 복구 불가능한 손상의 경우 회원은 회사에게 회사가 정한 해당 제품의 판매 가격 만큼의 비용을 지불하고 손상된 해당 물품에 대한 소유권을 취득합니다.</li>
          <li>3. 제품을 분실하였을 경우 손해배상 금액은 본 조의 제 2 항을 따릅니다.</li>
        </ol>

