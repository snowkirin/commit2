        <ol>
          <li>1. 회원의 멤버십이 해지 혹은 종료 시, 마지막으로 수령한 제품을 지정된 반납일로부터 2 영업일 내 반납하지 아니하거나, 회사의 반환 청구에 응하지 않을 경우에는 회사는 제품의 회수 및 손해 보전에 필요한 조치를 취할 수 있습니다.</li>
          <li>2. 회사는 제 1 항에 해당하는 경우, 제품의 소재 확인을 위해 회원에게 전화를 하거나 주소지를 방문하여 제품의 반환을 요구 할 수 있습니다. 이때 회원의 소재 불명 혹은 연락의 불가로 인하여 정당한 절차를 진행 할 수 없을 경우 , 도난 신고 등 필요한 조치를 취할 수 있습니다.</li>
          <li>3. 제 2 항의 조치에도 불구하고, 멤버십 해지 혹은 종료로부터 5 영업일이 경과하도록 제품의 반납이 이뤄지지 않거나 회사와 연락이 되지 않을 경우, 점유하고 있는 제품을 회사가 정한 판매가에 따라 등록된 결제 수단으로 자동 결제 합니다.</li>
          <li>4. 회사는 제 1 항 내지 제 2 항에 의거하여 발생한 회사의 손해 일체에 대해 배상을 청구 할 수 있으며, 해당 손해 배상액은 제품의 회수 및 소재 확인 등에 소요된 비용을 포함합니다.</li>
          <li>5. 회사는 해지 혹은 종료된 회원이 제 2 항에 조치 과정에서 연락이 되어, 제품을 반납하기로 할 경우 지정된 반납일로부터 1 일 당, 2 만원의 연체료를 부과하고 비용은 등록 된 결제 수단으로 결제 할 수 있습니다.</li>
          <li>6. 회사는 제 1 항 내지 제 3 항에 의한 업무를 처리하는 과정에서 고의 또는 과실로 회원에게 손해를 입힌 경우에는 배상 책임을 부담합니다.</li>
        </ol>

