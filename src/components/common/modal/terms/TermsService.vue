          <li>2. 등록 신용 카드의 한도 초과 , 카드 사용 중지 등의 기타 사유로 인하여 결제가 정상적으로 처리되지 않았음에도 회원이 등록 결제 수단을 변경하지 않거나 멤버십을 해지하지 않는 경우, 유효한 결제 수단이 등록될 때까지 회원의 서비스 이용이 제한될 수 있습니다. 회원이 등록 결제 수단을 변경하고 미납 금액을 납부할 책임이 있는 경우, 회사는 등록 결제 수단으로 청구를 지속할 수 있습니다. 이와 같은 이유들로 인해 결제 청구 일자에 변동이 발생할 수도 있습니다.</li>
        </ol>

        <span class="tit">제 14 조 멤버십의 해지 및 박탈</span>
        <ol>
          <li>1. 멤버십을 해지 요청하는 경우, 멤버십은 현재 이용 중인 멤버십 기간의 종료 시점에 자동적으로 해지 처리됩니다. 단, 사용 중인 상품의 반납 지연 혹은 해당 상품의 검수 후 상품의 훼손이 있을 시, 별도의 추가 과금이 이뤄질 수 있습니다.</li>
          <li>2. 멤버십 이용 중 해지를 요청 하는 경우, 두번째 구독 상품 배송 전에 한하여 잔여 2 주에 대한 해지만 가능합니다. 이때 해지로 인한 환불은 기 납부된 요금의 50% 입니다. 단, 첫번째 구독 상품의 반납이 이뤄지지 않거나, 상품의 훼손이 있을 경우, 별도의 추가 과금이 이뤄질 수 있습니다.</li>
          <li>3. 본 서비스와 관련하여 발생한 미납요금, 손해배상 등 회원의 회사에 대한 채무가 모두 정산될 때까지, 해당 회원의 줄라이 서비스 사용이 중지되며, 그 채무가 일정기간 계속하여 변제되지 않을 경우에는 해당 회원의 멤버십 자격이 박탈될 수 있습니다. 이러한 회원의 줄라이 사용 권한에 대한 판단 기준은 줄라이의 내부 운영 규정에 근거합니다.</li>
        </ol>

        <span class="tit">제 15 조 멤버십의 내용</span>
        <ol>
          <li>1. 회원은 줄라이 멤버십에 가입했을 경우, 줄라이가 보유한 제품 중 회원에 가장 적합한 상품을 추천하고, 줄라이의 내부 기준에 따라 회원의 선택 혹은 자동 선택하여 상품을 정해진 시점에 회원이 기입한 주소에 배송합니다.</li>
          <li>2. 회원은 상품을 받기로 지정한 날에 맞춰 이미 배송 받은 상품을 배송시와 동일한 상태로 포장하여, 상품 배송과 동시에 반납이 이뤄질 수 있게 해야 합니다. 만약 회원이 부득이한 사유로 지정한 배송일에 상품의 반납을 할 수 없는 경우, 지정한 배송일 1 일전(오후 2 시)까지 회사에 그 사실을 통지하여야 합니다. 해당 통지 시한을 지나 반납일의 변경을 요청하거나, 정해진 시점에 사전 통지 없이 반납이 이뤄지지 않을 경우, 회사는 회원에게 추가 회수를 위한 배송비 5 천원을 부과할 수 있습니다. 이때 해당 비용은 등록 결제 수단으로 자동 결제 됩니다.</li>
          <li>3. 멤버십 회원의 경우, 현재 배송 받은 상품을 반납하지 않고 구매를 원할 경우, 줄라이 사이트에서 해당 제품의 구매 기능을 통해 반납 없이 구매를 할 수 있습니다. 해당 제품의 구매는 사용 후 구매로서 반품, 교환이 불가능 합니다.</li>
          <li>4. 위 제 1 항 에서 제 3 항 까지의 내용은 회원이 멤버십을 해지할 때까지 적용됩니다. 회원은 멤버십을 해지했을 경우, 멤버십 이용기간이 종료되는 날까지 제2항과 같이 반납을 해야 합니다. 만약 공휴일등의 사유로 해당일에 반납이 불가능 할 경우 종료일 후에 반납수거가 가능한 가장 가까운 날로 반납일이 변경될 수 있습니다.</li>
        </ol>

        <span class="tit">제 16 조 제품 손상 및 분실의 조치</span>
        <ol>
          <li>1. 회원은 대여 중 제품의 손상 또는 분실을 발견한 때에는 회사에 연락하고 그 처리방향에 대하여 협의하여야 합니다.</li>
          <li>2. 회원의 고의ㆍ과실로 제품의 손상 또는 분실이 발생한 경우 손해보상 비용은 제 1 7 조에 따릅니다.</li>
          <li>3. 측정된 손해배상 비용은 등록 결제 수단으로 자동 결제됩니다.</li>
          <li>4. 회원은 제품 수령 이전의 손상이나 사이즈가 맞이 않아 제품을 사용 불능할 때는 제품 수령 후 1 영업일 이내에 회사에 통지 해야 하며, 회사는 회원께 해당 제품의 교체를 하거나 이에 준하는 조치를 제공하여야 합니다..</li>
